    private void somarValorTotalProdutos() {
        double soma = 0, valor;
        int cont = tblVendas.getRowCount();
        for(int i = 0; i < cont; i++) {
            valor = (double) tblVendas.getValueAt(i, 3);
            soma = soma + valor;
        }
        txtValorTotal.setText(String.valueOf(soma));
    }
